"""Question 3 Create Sales Data"""

import pandas as pd

sales_data = {
    "Month": ["Jan", "Feb", "Mar"],
    "Product_A": [10000, 12000, 9000],
    "Product_B": [15000, 13000, 11000],
    "Product_C": [8000, 9500, 7000]
}

sales_df = pd.DataFrame(sales_data)

print("Original Sales Data (Wide Format):")
print(sales_df)

"""Use melt() (Wide ➝ Long)"""

melted_df = pd.melt(
    sales_df,
    id_vars=["Month"],
    var_name="Product",
    value_name="Sales"
)

print("\nMelted Data (Long Format):")
print(melted_df)

"""Use pivot() (Long ➝ Wide)"""

pivot_df = melted_df.pivot(
    index="Month",
    columns="Product",
    values="Sales"
)

print("\nPivoted Data (Back to Wide Format):")
print(pivot_df)