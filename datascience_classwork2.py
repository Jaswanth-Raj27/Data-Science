inner_merge = pd.merge(students_df, marks_df, on="student_id", how="inner")
print("INNER JOIN:")
print(inner_merge)

outer_merge = pd.merge(students_df, marks_df, on="student_id", how="outer")
print("\nOUTER JOIN:")
print(outer_merge)


left_merge = pd.merge(students_df, marks_df, on="student_id", how="left")
print("\nLEFT JOIN:")
print(left_merge)

right_merge = pd.merge(students_df, marks_df, on="student_id", how="right")
print("\nRIGHT JOIN:")
print(right_merge)

concat_vertical = pd.concat([students_df, marks_df], axis=0)
print("\nCONCAT (Vertical):")
print(concat_vertical)


concat_horizontal = pd.concat([students_df, marks_df], axis=1)
print("\nCONCAT (Horizontal):")
print(concat_horizontal)

